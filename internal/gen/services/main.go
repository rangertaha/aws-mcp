// Command services generates internal/awsx/registry/zz_generated_clients.go
// from services.json: for every {name: importPath} entry it emits an
// aliased import and a ClientFactory registry entry calling
// <alias>.NewFromConfig. This is the ONLY place a new AWS service needs a
// literal import — everything else (operation discovery, schemas,
// dispatch) is derived from the resulting client type via reflection.
//
// Run via `go run ./internal/gen/services` (wired as `make generate`), from
// the repository root.
package main

import (
	"encoding/json"
	"fmt"
	"go/format"
	"go/token"
	"os"
	"sort"
	"strings"
	"text/template"
)

const (
	defaultServicesPath = "internal/gen/services/services.json"
	outputPath          = "internal/awsx/registry/zz_generated_clients.go"
)

const tmplSrc = `// Code generated by internal/gen/services from services.json; DO NOT EDIT.
// SPDX-License-Identifier: MIT

package registry

import (
	awssdk "github.com/aws/aws-sdk-go-v2/aws"
{{range .}}	{{.Alias}} "{{.Import}}"
{{end}}
)

// Factories maps every configured AWS service name to a constructor for its
// SDK client. Regenerate with: go run ./internal/gen/services (or
// "make generate") after editing services.json.
var Factories = map[string]ClientFactory{
{{range .}}	"{{.Name}}": func(cfg awssdk.Config) any { return {{.Alias}}.NewFromConfig(cfg) },
{{end}}}
`

type entry struct {
	Name   string
	Import string
	Alias  string
}

func main() {
	if err := run(os.Args[1:]); err != nil {
		fmt.Fprintln(os.Stderr, "gen/services:", err)
		os.Exit(1)
	}
}

func run(args []string) error {
	servicesPath := defaultServicesPath
	if len(args) > 0 {
		servicesPath = args[0]
	}

	entries, err := loadEntries(servicesPath)
	if err != nil {
		return err
	}

	src, err := render(entries)
	if err != nil {
		return err
	}

	if err := os.WriteFile(outputPath, src, 0o644); err != nil {
		return fmt.Errorf("writing %s: %w", outputPath, err)
	}
	fmt.Printf("gen/services: wrote %s (%d services)\n", outputPath, len(entries))
	return nil
}

func loadEntries(servicesPath string) ([]entry, error) {
	raw, err := os.ReadFile(servicesPath)
	if err != nil {
		return nil, fmt.Errorf("reading %s: %w", servicesPath, err)
	}

	var services map[string]string
	if err := json.Unmarshal(raw, &services); err != nil {
		return nil, fmt.Errorf("parsing %s: %w", servicesPath, err)
	}
	if len(services) == 0 {
		return nil, fmt.Errorf("%s declares no services", servicesPath)
	}

	entries := make([]entry, 0, len(services))
	for name, importPath := range services {
		// The service name doubles as the import alias, so it must be a
		// valid Go identifier — checked explicitly rather than assumed,
		// since services.json is hand-edited. Without this check, a bad
		// entry (a hyphen, a leading digit) would only surface much later
		// as an opaque gofmt parse error out of render(), pointing at
		// generated source instead of the actual bad entry in
		// services.json.
		if !token.IsIdentifier(name) {
			return nil, fmt.Errorf("%s: %q is not a valid Go identifier, required since it's used as the import alias", servicesPath, name)
		}
		entries = append(entries, entry{Name: name, Import: importPath, Alias: name})
	}
	sort.Slice(entries, func(i, j int) bool { return entries[i].Name < entries[j].Name })
	return entries, nil
}

func render(entries []entry) ([]byte, error) {
	tmpl, err := template.New("clients").Parse(tmplSrc)
	if err != nil {
		return nil, err
	}

	var buf strings.Builder
	if err := tmpl.Execute(&buf, entries); err != nil {
		return nil, err
	}

	formatted, err := format.Source([]byte(buf.String()))
	if err != nil {
		return nil, fmt.Errorf("formatting generated source: %w\n%s", err, buf.String())
	}
	return formatted, nil
}
